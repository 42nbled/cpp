#ifndef EASYFIND_HPP
#define EASYFIND_HPP

#include <iostream>
#include <algorithm>

template<typename T>
void easyfind(T var, int i)
{
    if (std::find(var.begin(), var.end(), i) != var.end())
		std::cout << "Found" << std::endl;
	else
		std::cout << "Not found" << std::endl;
}

#endif
